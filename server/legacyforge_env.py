_DOCS_CACHE_PATH = os.path.join(os.path.dirname(__file__), "static_docs_cache.json")


# ---------------------------------------------------------------------------
# HistoryAggregator
# ---------------------------------------------------------------------------
class HistoryAggregator:
    def __init__(self):
        self.async_errors = 0
        self.pydantic_errors = 0
        self.route_errors = 0
        self.middleware_errors = 0
        self.lifespan_errors = 0

    def update_from_output(self, output: str):
        """Parse pytest output and categorise failures into buckets."""
        text = output.lower()
        if "async" in text or "coroutine" in text:
            self.async_errors += 1
        if "pydantic" in text or "validationerror" in text or "response_model" in text:
            self.pydantic_errors += 1
        if "404" in text or "422" in text or "route" in text or "url" in text:
            self.route_errors += 1
        if "middleware" in text:
            self.middleware_errors += 1
        if "lifespan" in text or "startup" in text or "shutdown" in text:
            self.lifespan_errors += 1


